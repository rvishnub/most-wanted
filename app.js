function orderByDate(array, dateProp)
{
	array = cleanArray(array);
	for (objectIndex in array)
	{
		convertObjectDOBToDate(array[objectIndex]);
	}
	return array.slice().sort(function (a, b)
	{
    return (a[dateProp])< b[dateProp]? -1 : 1;
	});
}

